export const getTranslator = (lang: Language) => {
  return (key: TranslationKey, defaultText?: string): string => {
    const translationSet = translations[lang] || translations.en;
    const translatedText = translationSet[key];
    
    if (translatedText === undefined) {
      // Fallback to English if key is not found in the current language
      const fallbackText = translations.en[key];
      return fallbackText || defaultText || key;
    }
    
    return translatedText;
  };
};